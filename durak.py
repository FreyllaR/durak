import random


def создать_колоду():
    """Создать и перемешать колоду из 36 карт."""
    масти = ['черви', 'бубны', 'пики', 'трефы']
    значения = ['6', '7', '8', '9', '10', 'валет', 'дама', 'король', 'туз']
    колода = [{'масть': масть, 'значение': значение} for масть in масти for значение in значения]
    random.shuffle(колода)
    return колода


